package shataj.durbar.rrrrrr;

public class childitem {

  String id;
  String title;

    public childitem() {

    }


    public childitem(String id, String title) {
        this.id = id;
        this.title = title;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
